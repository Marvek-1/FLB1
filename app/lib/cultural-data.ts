export const validateStoryContent = (content: string, minWords: number, maxWords: number) => {
  const wordCount = content
    .trim()
    .split(/\s+/)
    .filter((word) => word.length > 0).length
  return {
    isValid: wordCount >= minWords && wordCount <= maxWords,
    wordCount,
    minWords,
    maxWords,
  }
}
